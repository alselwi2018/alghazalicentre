    {
        return serialize([
            $this->id,
            $this->password,
            $this->type,
            $this->email,
            $this->firstname,
            $this->lastname,
            $this->profession
        ]);
    }
    public function unserialize($serialized)
    {
        list(
            $this->id,
            $this->password,
            $this->type,
            $this->email,
